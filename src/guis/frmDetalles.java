    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed

        
        
        if (rbLecheDescremada.isSelected() == true) {
            this.productoCarrito.setSabor(this.productoCarrito.getSabor() + " Descremado");
        } else if (rbLecheDeslactosada.isSelected() == true) {
            this.productoCarrito.setSabor(this.productoCarrito.getSabor() + " Deslactosado");
        } else if (rbLecheRegular.isSelected() == true) {
            this.productoCarrito.setSabor(this.productoCarrito.getSabor() + " Regular");
        }

        Producto producto = CtrlProductos.getInstance().consultarPorNombre(productoCarrito.getNombre());
        
        if (rbTamanhoGrande.isSelected() == true) {
            this.productoCarrito.setTamaño("Grande");
            this.productoCarrito.setPrecio(producto.getPrecioVenta()*1.25f);
        } else if (rbTamanhoMediano.isSelected() == true) {
            this.productoCarrito.setTamaño("Mediano");
            this.productoCarrito.setPrecio(producto.getPrecioVenta());
        } else if (rbTamanhoJumbo.isSelected() == true) {
            this.productoCarrito.setTamaño("Jumbo");
            this.productoCarrito.setPrecio(producto.getPrecioVenta()*1.5f);
        }
        
        productoCarrito.setNota(txtNota.getText());
        if(bgLeche.getSelection() == null)
        {
            JOptionPane.showMessageDialog(null, "Debes seleccionar el tipo de leche.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        if(bgTamanho.getSelection() == null)
        {
            JOptionPane.showMessageDialog(null, "Debes seleccionar el tamaño.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        if(bgLeche.getSelection() != null && bgTamanho.getSelection() != null)
        {
            continuar();
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

